SELECT
    RUT,
    VERSIONDATA,
    SUM(CASE WHEN AGNO = 2024 AND MES = 1 THEN RENTABRUTA ELSE 0 END) AS "2024-01",
    SUM(CASE WHEN AGNO = 2024 AND MES = 2 THEN RENTABRUTA ELSE 0 END) AS "2024-02",
    SUM(CASE WHEN AGNO = 2024 AND MES = 3 THEN RENTABRUTA ELSE 0 END) AS "2024-03",
    SUM(CASE WHEN AGNO = 2024 AND MES = 4 THEN RENTABRUTA ELSE 0 END) AS "2024-04",
    SUM(CASE WHEN AGNO = 2024 AND MES = 5 THEN RENTABRUTA ELSE 0 END) AS "2024-05",
    SUM(CASE WHEN AGNO = 2024 AND MES = 6 THEN RENTABRUTA ELSE 0 END) AS "2024-06",
    SUM(CASE WHEN AGNO = 2024 AND MES = 7 THEN RENTABRUTA ELSE 0 END) AS "2024-07",
    SUM(CASE WHEN AGNO = 2024 AND MES = 8 THEN RENTABRUTA ELSE 0 END) AS "2024-08",
    SUM(CASE WHEN AGNO = 2024 AND MES = 9 THEN RENTABRUTA ELSE 0 END) AS "2024-09",
    SUM(CASE WHEN AGNO = 2024 AND MES = 10 THEN RENTABRUTA ELSE 0 END) AS "2024-10",
    SUM(CASE WHEN AGNO = 2024 AND MES = 11 THEN RENTABRUTA ELSE 0 END) AS "2024-11",
    SUM(CASE WHEN AGNO = 2024 AND MES = 12 THEN RENTABRUTA ELSE 0 END) AS "2024-12",
    SUM(CASE WHEN AGNO = 2025 AND MES = 1 THEN RENTABRUTA ELSE 0 END) AS "2025-01",
    SUM(CASE WHEN AGNO = 2025 AND MES = 2 THEN RENTABRUTA ELSE 0 END) AS "2025-02",
    SUM(CASE WHEN AGNO = 2025 AND MES = 3 THEN RENTABRUTA ELSE 0 END) AS "2025-03",
    SUM(CASE WHEN AGNO = 2025 AND MES = 4 THEN RENTABRUTA ELSE 0 END) AS "2025-04",
    SUM(CASE WHEN AGNO = 2025 AND MES = 5 THEN RENTABRUTA ELSE 0 END) AS "2025-05",
    SUM(CASE WHEN AGNO = 2025 AND MES = 6 THEN RENTABRUTA ELSE 0 END) AS "2025-06",
    SUM(CASE WHEN AGNO = 2025 AND MES = 7 THEN RENTABRUTA ELSE 0 END) AS "2025-07",
    SUM(CASE WHEN AGNO = 2025 AND MES = 8 THEN RENTABRUTA ELSE 0 END) AS "2025-08",
    SUM(CASE WHEN AGNO = 2025 AND MES = 9 THEN RENTABRUTA ELSE 0 END) AS "2025-09",
    SUM(CASE WHEN AGNO = 2025 AND MES = 10 THEN RENTABRUTA ELSE 0 END) AS "2025-10",
    SUM(CASE WHEN AGNO = 2025 AND MES = 11 THEN RENTABRUTA ELSE 0 END) AS "2025-11",
    SUM(CASE WHEN AGNO = 2025 AND MES = 12 THEN RENTABRUTA ELSE 0 END) AS "2025-12"
FROM
    T_EMPLEADORENTA
WHERE
    VERSIONDATA = 1
    AND AGNO IN (2024, 2025)
GROUP BY
    RUT,
    VERSIONDATA
ORDER BY
    RUT,
    VERSIONDATA;